package entities;

public class Department {

	//Atributos
	private String name;
	
	//Construtores
	public Department() {
		
	}
	
	//Get setters

	public Department(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}	
}
